    private void btnSaveCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveCarActionPerformed
        // TODO add your handling code here:
        if(isValidData() == true){ 
            Car car = new Car();
            //System.out.println(txtCarName.getText());
            car.setCarName(txtCarName.getText());
            car.setBrandName(comboBrandName.getSelectedItem().toString());
            car.setModelNumber(txtModelNumber.getText());
            car.setSerialNumber(txtSerialNumber.getText().toUpperCase());
            car.setManufactureYear(Integer.parseInt(txtManufacturingYear.getText()));
            car.setMaxNumberOfSeats(Integer.parseInt(txtMaxNumberOfSeats.getText()));
            car.setMinNumberOfSeats(Integer.parseInt(txtMinNumberOfSeats.getText()));
            car.setIsAvailable(chkYesAvailable.isSelected());
            car.setCity(comboCity.getSelectedItem().toString());
            car.lastUpdated = new Date();
            if(chkCertified.isSelected())
                car.setCertificateExpiration("Certified");
            else{
                car.setCertificateExpiration("Expired");
            }
            cars.add(car);
            JOptionPane.showMessageDialog(this, "Car add successfully.");
        }
    }//GEN-LAST:event_btnSaveCarActionPerformed

